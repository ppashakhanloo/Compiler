	public void parse() throws Exception {
		int token;
		// Integer topStack = new Integer(0);
		parseStack.push(new Integer(0));

		token = scanner.NextToken();

		while (true) {
			printStack();
			System.out.println("TOKEN=" + token);
			System.out.println("TOPSTACK=" + topStack());
			if (ActionTable_S[topStack()][token] != -1) {
				System.out.println("IN ACTION TABLE S");
				parseStack.push(ActionTable_S[topStack()][token]);
				token = scanner.NextToken();
				System.out.println("next token= " + token);
			} else if (ActionTable_R[topStack()][token] != -1) {
				System.out.println("IN ACTION TABLE R");

				if (ActionTable_R[topStack()][token] == 0) {
					System.out.println("ACCEPT");
					return;
				}

				int oldTopStack = topStack();
				for (int i = 0; i < RHS[ActionTable_R[oldTopStack][token]]; i++) {
					parseStack.pop();
				}
				parseStack
						.push(GotoTable[topStack()][LHS[ActionTable_R[oldTopStack][token]]]);

				// TODO CALL CODE GENERATOR

			} else {

				throw new CompilerException("Error occured during parse.");
			}
		}

	}

